package projetoJAVA;

public class Dia {

protected int dia;
protected boolean concluido;

public Dia(int dia){
	this.dia=dia;
}
public int getDia() {
	return dia;
}
public void setDia(int dia) {
	this.dia = dia;
}
public boolean isConcluido() {
	return concluido;
}
public void setConcluido(boolean concluido) {
	this.concluido = concluido;
}
}
